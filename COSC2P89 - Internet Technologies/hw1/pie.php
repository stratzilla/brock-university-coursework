		<h1>Grandma's Lemon Meringue Pie</h1>
		<img src="http://www.cosc.brocku.ca/Offerings/2P89/Images/pie.jpg"><br>
		<p>
			One 9-inch pie<br>
			30 Min - Prep time<br>
			10 Min - Cook time<br>
			40 Min - Total<br>
			8 Servings</br>
		</p>
		<hr/>
		<h2>INGREDIENTS</h2>
		<ul>
			<li>1 cup white sugar
			<li>2 <abbr title="tablespoon">tbsp</abbr> all-purpose flour
			<li>3 <abbr title="tablespoon">tbsp</abbr> cornstarch
			<li>1/4 <abbr title="teaspoon">tsp</abbr> salt
			<li>1 1/2 cups water
			<li>2 lemons, juiced and zested
			<li>2 <abbr title="tablespoon">tbsp</abbr> butter
			<li>4 egg yolks, beaten
			<li>1 (9 inch) pie crust, baked
			<li>4 egg whites
			<li>6 <abbr title="tablespoon">tbsp</abbr> white sugar
		</ul>
		<hr/>
		<h2>DIRECTIONS</h2>
		<ol>
			<li><b>Preheat Oven:</b> Projeat oven to 350 degrees F (175 degrees C).
			<li><b>Make Lemon Filling:</b> In a medium saucepan ...
			<ul class="a">
				<li>Whisk together 1 cup sugar, flour, cornstarch, and salt.
				<li>Stir in water, lemon juice and lemon zest.
				<li>Cook over medium-high heat, stirring frequently, until mixture comes to a boil.
				<li>Stir in butter.
				<li>Place egg yolks in a small bowl and gradually whisk in 1/2 cup of hot sugar mixture.
				<li>Whisk egg yolk mixture back into remaining sugar mixture.
				<li>Bring to a boil and continue to cook while stirring constantly until thick.
				<li>Remove from heat.
				<li>Pour filling into baked pastry shell.
			</ul>
			<li><b>Make Meringue:</b> In a large glass or metal bowl ...
			<ul class="a">
				<li>Whip egg whites until foamy.
				<li>Add sugar gradually, and continue to whip until stiff peaks form.
				<li>Spread meringue over pie, sealing the edges at the crust.
			</ul>
			<li><b>Bake:</b> Bake in preheated oven for 10 minutes, or until meringue is golden brown.
		</ol>
