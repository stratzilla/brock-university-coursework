package com.example.b.bc;

public class Users {
    String id;
    String name;

    // constructors

    public Users() {}

    public Users(String id, String name) {
        this.id = id;
        this.name = name;
    }

    // getter methods

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
